//  methods for arrays
// push() -- adds an item at the end 
//  pop() -- removes the last item 
// shift() -- removes the first item 
// unshift() -- adds an item at the beginning 

let numbers = [1,2,3,4];

numbers.push(5);
console.log(numbers);

numbers.pop();
console.log(numbers);

numbers.shift();
console.log(numbers);

numbers.unshift(1);
console.log(numbers);
